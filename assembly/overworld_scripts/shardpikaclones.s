.global EventScript_Random_GivePika
EventScript_Random_GivePika:
    lock
    faceplayer
    msgbox gText_RandomPika_1 MSG_FACE
    checkitem 0x32 0x1 
    compare 0x800D 0x1
    if greaterorequal _goto YouHaveShit @red
    release
    end

NahImGood:
    msgbox gText_RandomEgg_AwOkayThen MSG_FACE
    release
    end

YouHaveShit:
    msgbox gText_RandomPika_2 MSG_YESNO
    compare LASTRESULT 0
    if equal _goto NahImGood
    goto GivePika 
    end

GivePika:
    special2 LASTRESULT 0x83
    compare LASTRESULT 0x6
    if notequal _goto ReceivePika  @if we have a slot open 
    msgbox gText_RandomEgg_NoRoom MSG_FACE
    release 
    end

ReceivePika:
    removeitem 0x32 0x1
    call EggMessageStuff
    random 0xF
    compare 0x800D 0x0
    if equal _goto Surfing
    compare 0x800D 0x1 
    if equal _goto Flying
    compare 0x800D 0x2
    if equal _goto Cosplay
    compare 0x800D 0x3
    if equal _goto Libre
    compare 0x800D 0x4
    if equal _goto PopStar
    compare 0x800D 0x5
    if equal _goto RockStar 
    compare 0x800D 0x6 
    if equal _goto Belle 
    compare 0x800D 0x7
    if equal _goto PhD
    compare 0x800D 0x8
    if equal _goto Original 
    compare 0x800D 0x9 
    if equal _goto Hoenn 
    compare 0x800D 0xA
    if equal _goto Sinnoh 
    compare 0x800D 0xB
    if equal _goto Unova 
    compare 0x800D 0xC 
    if equal _goto Kalos 
    compare 0x800D 0xD 
    if equal _goto Alola 
    compare 0x800D 0xE
    if equal _goto Partner 
    end

